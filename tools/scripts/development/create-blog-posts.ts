async function createOrReplaceAuthor(author: any) {
  try {
    // Check if author exists
    const existingAuthor = await enhancedClient.fetch(
      `*[_type == "author" && slug.current == $slug][0]`,
      { slug: author.slug }
    );

    if (existingAuthor) {
      // Update existing author
      const result = await enhancedClient
        .patch(existingAuthor._id)
        .set(author)
        .commit();
      console.log(`‚úÖ Updated author: ${author.name}`);
      return result;
    } else {
      // Create new author
      const result = await enhancedClient.create({
        _type: 'author',
        ...author,
      });
      console.log(`‚úÖ Created author: ${author.name}`);
      return result;
    }
  } catch (error) {
    console.error(`‚ùå Error creating/updating author ${author.name}:`, error);
    throw error;
  }
}

